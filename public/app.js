const form = document.querySelector("#analyze-form");
const textForm = document.querySelector("#text-form");
const fileForm = document.querySelector("#file-form");
const statusEl = document.querySelector("#status");
const resultsEl = document.querySelector("#results");
const thumbnailEl = document.querySelector("#thumbnail");
const titleEl = document.querySelector("#video-title");
const authorEl = document.querySelector("#video-author");
const ageRatingEl = document.querySelector("#age-rating");
const profanityTotalEl = document.querySelector("#profanity-total");
const wordCountEl = document.querySelector("#word-count");
const captionSourceEl = document.querySelector("#caption-source");
const analysisProviderEl = document.querySelector("#analysis-provider");
const summaryEl = document.querySelector("#summary");
const reasonsEl = document.querySelector("#reasons");
const topicsEl = document.querySelector("#topics");
const profanityListEl = document.querySelector("#profanity-list");
const transcriptEl = document.querySelector("#transcript");
const copyButton = document.querySelector("#copy-transcript");

function setStatus(message, type = "idle") {
  statusEl.textContent = message;
  statusEl.dataset.type = type;
}

function formatDuration(seconds) {
  if (!seconds) return "";
  const minutes = Math.floor(seconds / 60);
  const remaining = seconds % 60;
  return `${minutes}:${String(remaining).padStart(2, "0")}`;
}

function renderChips(container, items, emptyText) {
  container.innerHTML = "";

  if (!items.length) {
    const empty = document.createElement("span");
    empty.className = "empty";
    empty.textContent = emptyText;
    container.appendChild(empty);
    return;
  }

  for (const item of items) {
    const chip = document.createElement("span");
    chip.className = "chip";
    chip.textContent = typeof item === "string" ? item : `${item.label || item.term} (${item.count})`;
    container.appendChild(chip);
  }
}

function renderResults(data) {
  const { metadata, analysis, captions, transcript } = data;

  thumbnailEl.src = metadata.thumbnail;
  titleEl.textContent = metadata.title;
  authorEl.textContent = `${metadata.author}${metadata.durationSeconds ? ` - ${formatDuration(metadata.durationSeconds)}` : ""}`;

  ageRatingEl.textContent = analysis.ageRating;
  profanityTotalEl.textContent = analysis.profanityTotal;
  wordCountEl.textContent = analysis.wordCount;
  captionSourceEl.textContent = captions.source === "huggingface-audio" ? "Audio HF" : captions.isAutoGenerated ? "Auto" : "Manual";
  analysisProviderEl.textContent = analysis.provider === "huggingface" ? "HF" : "Regras";
  summaryEl.textContent = analysis.summary;
  transcriptEl.textContent = transcript;

  reasonsEl.innerHTML = "";
  for (const reason of analysis.reasons) {
    const li = document.createElement("li");
    li.textContent = reason;
    reasonsEl.appendChild(li);
  }

  renderChips(topicsEl, analysis.topics, "Nenhum tema sensivel detectado.");
  renderChips(profanityListEl, analysis.profanity, "Nenhum palavrao encontrado.");

  resultsEl.classList.remove("hidden");
}

form.addEventListener("submit", async (event) => {
  event.preventDefault();
  const submitButton = form.querySelector("button");
  const url = new FormData(form).get("url");

  submitButton.disabled = true;
  resultsEl.classList.add("hidden");
  setStatus("Buscando legenda. Se nao tiver, vou transcrever o audio com Hugging Face...", "loading");

  try {
    const response = await fetch("/api/analyze", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ url }),
    });

    const data = await response.json();
    if (!response.ok) throw new Error(data.error || "Nao foi possivel analisar o video.");

    renderResults(data);
    setStatus("Analise concluida.", "success");
  } catch (error) {
    setStatus(error.message, "error");
  } finally {
    submitButton.disabled = false;
  }
});

textForm.addEventListener("submit", async (event) => {
  event.preventDefault();
  const submitButton = textForm.querySelector("button");
  const transcript = new FormData(textForm).get("transcript");

  submitButton.disabled = true;
  resultsEl.classList.add("hidden");
  setStatus("Analisando a transcricao colada...", "loading");

  try {
    const response = await fetch("/api/analyze-text", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ transcript }),
    });

    const data = await response.json();
    if (!response.ok) throw new Error(data.error || "Nao foi possivel analisar o texto.");

    renderResults(data);
    setStatus("Analise do texto concluida.", "success");
  } catch (error) {
    setStatus(error.message, "error");
  } finally {
    submitButton.disabled = false;
  }
});

fileForm.addEventListener("submit", async (event) => {
  event.preventDefault();
  const submitButton = fileForm.querySelector("button");
  const file = fileForm.querySelector("input[type='file']").files[0];

  if (!file) {
    setStatus("Escolha um arquivo de video ou audio.", "error");
    return;
  }

  submitButton.disabled = true;
  resultsEl.classList.add("hidden");
  setStatus("Enviando arquivo para o Hugging Face transcrever e analisar...", "loading");

  try {
    const response = await fetch("/api/analyze-file", {
      method: "POST",
      headers: { "Content-Type": file.type || "application/octet-stream" },
      body: file,
    });

    const data = await response.json();
    if (!response.ok) throw new Error(data.error || "Nao foi possivel analisar o arquivo.");

    renderResults(data);
    setStatus("Analise do arquivo concluida.", "success");
  } catch (error) {
    setStatus(error.message, "error");
  } finally {
    submitButton.disabled = false;
  }
});

copyButton.addEventListener("click", async () => {
  await navigator.clipboard.writeText(transcriptEl.textContent);
  copyButton.textContent = "Copiado";
  window.setTimeout(() => {
    copyButton.textContent = "Copiar";
  }, 1400);
});
